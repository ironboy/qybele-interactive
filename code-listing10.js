function start() {
  let article, section;
  try {
    article = document.currentScript.parentElement.parentElement;
    section = article.querySelector('section.module_content');
  }
  catch (_e) { setTimeout(start, 20); return; }
  if (!section) { setTimeout(start, 20); return; }
  const p = document.createElement('p');
  p.innerText = 'This is some extra content generated by JavaScript.';
  section.append(p);
}

start();
